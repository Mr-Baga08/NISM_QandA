function App() {
  const [questions, setQuestions] = useState<Question[]>([]);
  const [currentQuestionIndex, setCurrentQuestionIndex] = useState(0);
  const [currentAnswers, setCurrentAnswers] = useState<{ [key: number]: string }>({});
  const [showResults, setShowResults] = useState(false);
  const [timeLeft, setTimeLeft] = useState(720); // 12 minutes in seconds
  const [quizStarted, setQuizStarted] = useState(false);

  useEffect(() => {
    if (quizStarted && timeLeft > 0 && !showResults) {
      const timer = setInterval(() => {
        setTimeLeft((prev) => prev - 1);
      }, 1000);
      return () => clearInterval(timer);
    } else if (timeLeft === 0 && !showResults) {
      setShowResults(true);
    }
  }, [timeLeft, quizStarted, showResults]);

  const startQuiz = () => {
    // Randomly select 10 questions
    const shuffled = [...allQuestions].sort(() => 0.5 - Math.random());
    setQuestions(shuffled.slice(0, 10));
    setQuizStarted(true);
    setCurrentAnswers({});
    setShowResults(false);
    setTimeLeft(720);
    setCurrentQuestionIndex(0);
  };

  const handleAnswer = (questionId: number, answer: string) => {
    setCurrentAnswers((prev) => ({
      ...prev,
      [questionId]: answer,
    }));
  };

  const nextQuestion = () => {
    if (currentQuestionIndex < questions.length - 1) {
      setCurrentQuestionIndex(prev => prev + 1);
    }
  };

  const previousQuestion = () => {
    if (currentQuestionIndex > 0) {
      setCurrentQuestionIndex(prev => prev - 1);
    }
  };

  const submitQuiz = () => {
    setShowResults(true);
  };

  const calculateScore = () => {
    let correct = 0;
    questions.forEach((q) => {
      if (currentAnswers[q.id] === q.correctAnswer) {
        correct += 4;
      } else if (currentAnswers[q.id]) {
        correct -= 1;
      }
    });
    return correct;
  };

  const isPassing = () => {
    const score = calculateScore();
    const maxScore = questions.length * 4;
    return (score / maxScore) * 100 >= 60;
  };

  const formatTime = (seconds: number) => {
    const mins = Math.floor(seconds / 60);
    const secs = seconds % 60;
    return `${mins}:${secs.toString().padStart(2, '0')}`;
  };

  if (!quizStarted) {
    return (
      <div className="min-h-screen bg-gray-100 flex items-center justify-center p-4">
        <div className="max-w-2xl w-full bg-white rounded-lg shadow-md p-8">
          <h1 className="text-3xl font-bold text-center mb-6">Securities Market Quiz</h1>
          <div className="space-y-4 text-center">
            <p className="text-gray-600">
               Test consists of 10 random questions<br />
               Time limit: 12 minutes<br />
               +4 marks for correct answer<br />
               -1 mark for wrong answer<br />
               0 marks for unattempted questions<br />
               60% required to pass
            </p>
            <button
              onClick={startQuiz}
              className="bg-blue-600 text-white px-6 py-2 rounded-md hover:bg-blue-700 transition-colors"
            >
              Start Quiz
            </button>
          </div>
        </div>
      </div>
    );
  }

  return (
    <div className="min-h-screen bg-gray-100 p-4">
      <div className="max-w-3xl mx-auto">
        <div className="bg-white rounded-lg shadow-md p-6 mb-4">
          <div className="flex justify-between items-center mb-4">
            <h1 className="text-2xl font-bold">Securities Market Quiz</h1>
            <div className="flex items-center gap-2 text-lg">
              <Timer className="w-6 h-6" />
              <span className={`font-mono ${timeLeft < 60 ? 'text-red-600' : ''}`}>
                {formatTime(timeLeft)}
              </span>
            </div>
          </div>

          {!showResults ? (
            <div className="space-y-6">
              <div className="flex justify-between items-center mb-4">
                <span className="text-sm text-gray-500">Question {currentQuestionIndex + 1} of {questions.length}</span>
                <div className="h-2 bg-gray-200 rounded-full w-48">
                  <div 
                    className="h-2 bg-blue-600 rounded-full transition-all duration-300"
                    style={{ width: `${((currentQuestionIndex + 1) / questions.length) * 100}%` }}
                  ></div>
                </div>
              </div>

              <div className="border-b pb-4">
                <p className="font-medium mb-3">
                  {questions[currentQuestionIndex]?.question}
                </p>
                <div className="space-y-2">
                  {questions[currentQuestionIndex]?.options.map((option) => (
                    <label
                      key={option}
                      className="flex items-center gap-2 cursor-pointer p-2 rounded hover:bg-gray-50"
                    >
                      <input
                        type="radio"
                        name={`question-${questions[currentQuestionIndex]?.id}`}
                        value={option}
                        checked={currentAnswers[questions[currentQuestionIndex]?.id] === option}
                        onChange={() => handleAnswer(questions[currentQuestionIndex]?.id, option)}
                        className="w-4 h-4 text-blue-600"
                      />
                      <span>{option}</span>
                    </label>
                  ))}
                </div>
              </div>

              <div className="flex justify-between mt-6">
                <button
                  onClick={previousQuestion}
                  disabled={currentQuestionIndex === 0}
                  className={`flex items-center gap-2 px-4 py-2 rounded-md transition-colors ${
                    currentQuestionIndex === 0
                      ? 'bg-gray-200 text-gray-500 cursor-not-allowed'
                      : 'bg-blue-600 text-white hover:bg-blue-700'
                  }`}
                >
                  <ArrowLeft className="w-4 h-4" />
                  Previous
                </button>

                <div className="flex gap-2">
                  {currentQuestionIndex === questions.length - 1 ? (
                    <button
                      onClick={submitQuiz}
                      className="bg-green-600 text-white px-6 py-2 rounded-md hover:bg-green-700 transition-colors"
                    >
                      Submit Quiz
                    </button>
                  ) : (
                    <button
                      onClick={nextQuestion}
                      className="bg-blue-600 text-white px-6 py-2 rounded-md hover:bg-blue-700 transition-colors flex items-center gap-2"
                    >
                      Next
                      <ArrowRight className="w-4 h-4" />
                    </button>
                  )}
                </div>
              </div>

              <div className="mt-6 pt-4 border-t">
                <div className="flex gap-2 flex-wrap">
                  {questions.map((_, idx) => (
                    <button
                      key={idx}
                      onClick={() => setCurrentQuestionIndex(idx)}
                      className={`w-8 h-8 rounded-full flex items-center justify-center text-sm font-medium transition-colors ${
                        idx === currentQuestionIndex
                          ? 'bg-blue-600 text-white'
                          : currentAnswers[questions[idx]?.id]
                          ? 'bg-green-100 text-green-800'
                          : 'bg-gray-100 text-gray-600 hover:bg-gray-200'
                      }`}
                    >
                      {idx + 1}
                    </button>
                  ))}
                </div>
              </div>
            </div>
          ) : (
            <div className="space-y-6">
              <div className="text-center p-6 bg-gray-50 rounded-lg">
                <h2 className="text-2xl font-bold mb-2">Quiz Results</h2>
                <p className="text-4xl font-bold text-blue-600">
                  Score: {calculateScore()}/40
                </p>
                <p className={`text-lg mt-2 ${isPassing() ? 'text-green-600' : 'text-red-600'}`}>
                  {isPassing() ? 'PASS' : 'FAIL'}
                </p>
              </div>
              
              {questions.map((q, idx) => (
                <div key={q.id} className="border-b pb-4 last:border-b-0">
                  <div className="flex items-start gap-2">
                    <span className="mt-1">
                      {currentAnswers[q.id] === q.correctAnswer ? (
                        <CheckCircle2 className="w-5 h-5 text-green-600" />
                      ) : (
                        <XCircle className="w-5 h-5 text-red-600" />
                      )}
                    </span>
                    <div>
                      <p className="font-medium mb-2">
                        {idx + 1}. {q.question}
                      </p>
                      <p className="text-sm text-gray-600">
                        Your answer: {currentAnswers[q.id] || "Not attempted"}
                      </p>
                      <p className="text-sm text-green-600">
                        Correct answer: {q.correctAnswer}
                      </p>
                    </div>
                  </div>
                </div>
              ))}
              
              <div className="flex justify-center mt-6">
                <button
                  onClick={startQuiz}
                  className="bg-blue-600 text-white px-6 py-2 rounded-md hover:bg-blue-700 transition-colors"
                >
                  Try Again
                </button>
              </div>
            </div>
          )}
        </div>
      </div>
    </div>
  );
}

export default App;